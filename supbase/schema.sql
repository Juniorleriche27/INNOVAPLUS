begin
  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'app'
      and table_name = 'rag_chunks'
      and column_name = 'embedding'
  ) and not exists (
    select 1
    from app.rag_chunks
    where embedding is not null
  ) then
    alter table app.rag_chunks
    alter column embedding type vector(1024);
  end if;
end;
$$;

