create or replace function app.match_rag_chunks(
  query_embedding vector(1024),
  match_count integer default 5,
  filter_corpus text default null
)
returns table (
  chunk_id uuid,
  document_id uuid,
  chunk_index integer,
  title text,
  source_file text,
  content text,
  metadata jsonb,
  score double precision
)
language sql
stable
as $$
  select
    c.id as chunk_id,
    c.document_id,
    c.chunk_index,
    c.title,
    c.source_file,
    c.content,
    c.metadata,
    1 - (c.embedding <=> query_embedding) as score
  from app.rag_chunks c
  join app.rag_documents d on d.id = c.document_id
  where c.embedding is not null
    and (
      filter_corpus is null
      or coalesce(d.metadata->>'corpus', '') = filter_corpus
    )
  order by c.embedding <=> query_embedding
  limit greatest(match_count, 1);
$$;

